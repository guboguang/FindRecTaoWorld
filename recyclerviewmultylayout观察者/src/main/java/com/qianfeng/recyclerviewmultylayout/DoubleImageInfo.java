public class DoubleImageInfo extends BaseInfo
{
    private int imgLeftUrl;

    private int imgRightUrl;

    public int getImgLeftUrl() {
        return imgLeftUrl;
    }

    public void setImgLeftUrl(int imgLeftUrl) {
        this.imgLeftUrl = imgLeftUrl;
    }

    public int getImgRightUrl() {
        return imgRightUrl;
    }

    public void setImgRightUrl(int imgRightUrl) {
        this.imgRightUrl = imgRightUrl;
    }
}
